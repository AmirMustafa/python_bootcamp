# Arithmetic Operator

# + Addition
print(1 + 2)

# - Subtraction
print(10 - 12)

# / Division - always returns float
print(8 / 2);

# // - equivalent to Math.floor() of JS

print(11 / 2)

# * - multiply
print(2 * 9)

# ** - exponential
print(2 ** 9)

# % - module i.e. remainder
print(7 % 3)

# arithmetic order
print(2 + 4 * 2 ** 3)


# big numbers for readability purpose we can add _ 
print(1_000_000_000)
